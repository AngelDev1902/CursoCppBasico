// #include permite importar librerias al programa para poder utilizar sus funciones
// libreria principal para el manejo de entrada y salida de datos
#include <iostream>

//  directiva que permite acceder a los elementos del espacio de nombres estándar (std)
using namespace std;

// Función principal del programa [todo el codigo dentro de la funcion es codigo ejecutable]
// todo trozo de codigo va contenido entre llaves {}
int main() {
    // cout es el objeto de salida estandar que permite imprimir en la consola el mensaje que se le pase
    // toda expresion termina con un punto y coma
    cout << "Hello, World!" << endl;
    
    // retorna 0 para indicar que el programa se ejecuto correctamente
    return 0;
}
